int		ft_compact(char **tab, int length)
{
	int i;
	int j;

	i = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			if (tab[i][j] == '0')
			{
				length = length - 1;
			}
			j++;
		}
		i++;
	}
	return (length);
}
